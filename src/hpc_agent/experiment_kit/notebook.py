"""``export_notebook`` — lift a ``.ipynb`` into a ``.py`` executor module.

hpc-agent has no notebook path of its own; a researcher with a notebook
otherwise hand-translates it into an executor. :func:`export_notebook`
does that mechanically, with a deliberately strict rule so the result is
predictable:

**Emit a top-level statement iff it is one of**

- ``import`` / ``from ... import``
- a function definition (``def`` / ``async def``)
- a class definition
- an assignment whose every target is an UPPERCASE name (module
  constants, e.g. ``WINDOW = 48``)

Everything else — smoke-test calls, plots, ``df = pd.read_csv(...)``
scratch lines, expression statements — is skipped **silently**. No cell
tags, no magic markers: the AST shape *is* the contract.

**Self-contained output.** When the notebook imports from
``hpc_agent.experiment_kit`` (i.e. it is a ``@register_run`` experiment), the
export runs in *inline mode*: the ``hpc_agent.experiment_kit`` import is
dropped and the source of :mod:`hpc_agent.experiment_kit._runtime` — the
self-contained, stdlib-only cluster runtime — is prepended verbatim. The
exported ``.py`` then imports nothing from ``hpc_agent`` and runs on a
stdlib-only cluster. Because the *actual* runtime source is inlined,
there is no copy to drift and no parity test to keep — same bytes by
construction.

Statements are emitted in source order (cell order, then within a cell).
A cell that does not parse is skipped whole.

**Format-agnostic cores.** The extraction rules operate on *cell sources*
(plain strings), not on the ``.ipynb`` container: :func:`export_cells` and
:func:`export_cells_markers` take a sequence of cell sources, and the
``export_notebook*`` functions are the ``.ipynb``-reading wrappers. A
jupytext percent-format ``.py`` notebook feeds the SAME cores — its cells
are read by :func:`hpc_agent.state.audit_source.percent_cell_sources` (the
one percent-format reader) at the ``export-package`` layer.
"""

from __future__ import annotations

import ast
import json
from pathlib import Path
from typing import TYPE_CHECKING

if TYPE_CHECKING:
    from collections.abc import Sequence

__all__ = [
    "export_cells",
    "export_cells_markers",
    "export_notebook",
    "export_notebook_markers",
    "notebook_imports_runtime",
]

_DEF_NODES = (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef)
_IMPORT_NODES = (ast.Import, ast.ImportFrom)

_RUNTIME_MODULE = "hpc_agent.experiment_kit"

# A code cell exports (whole cell) when one of its lines, stripped, is
# exactly this marker. The convention for the pipeline-library notebooks
# whose surface is not the strict-AST allowlist `export_notebook` uses.
_EXPORT_MARKER = "# export"
_INLINE_HEADER = (
    "# ─── inlined hpc_agent.experiment_kit runtime "
    "(self-contained; generated by export_notebook) ───\n"
    "# Do not edit. Re-run `make export` to regenerate from the notebook.\n"
)


def _code_cell_sources(ipynb: str | Path) -> list[str]:
    """The code-cell sources of *ipynb*, in notebook order.

    The one ``.ipynb`` reading seam for this module — every exporter and
    check below consumes cell sources through it.
    """
    data = json.loads(Path(ipynb).read_text(encoding="utf-8"))
    sources: list[str] = []
    for cell in data.get("cells", []):
        if cell.get("cell_type") != "code":
            continue
        src = cell.get("source", "")
        if isinstance(src, list):
            src = "".join(src)
        sources.append(src)
    return sources


def export_notebook(ipynb: str | Path, out_py: str | Path) -> Path:
    """Extract the importable surface of *ipynb* into *out_py*.

    Returns the path written. When the notebook is a ``@register_run``
    experiment the output is a self-contained executor (see the module
    docstring); otherwise it is a plain importable ``.py``. The
    ``.ipynb``-reading wrapper over :func:`export_cells`.
    """
    return export_cells(_code_cell_sources(ipynb), out_py)


def export_cells(cell_sources: Sequence[str], out_py: str | Path) -> Path:
    """Extract the importable surface of *cell_sources* into *out_py*.

    The format-agnostic strict-AST core (see the module docstring for the
    allowlist): *cell_sources* are plain code-cell strings, whether read
    from an ``.ipynb`` or from a percent-format ``.py``. Returns the path
    written.
    """
    collected: list[tuple[ast.stmt, str]] = []

    for src in cell_sources:
        if not src.strip():
            continue
        try:
            tree = ast.parse(src)
        except SyntaxError:
            continue
        lines = src.splitlines()
        for node in tree.body:
            if _is_exportable(node):
                segment = _segment(lines, node)
                if segment:
                    collected.append((node, segment))

    inline = any(_imports_runtime(node) for node, _ in collected)
    if inline:
        # Drop the hpc_agent.experiment_kit import (the inlined shim provides
        # those names) and any ``from __future__`` import (the shim
        # already carries one, and it must stay the first statement).
        kept = [
            seg
            for node, seg in collected
            if not _imports_runtime(node) and not _is_future_import(node)
        ]
        shim = (Path(__file__).resolve().parent / "_runtime.py").read_text(encoding="utf-8")
        parts = [_INLINE_HEADER + shim.rstrip(), *kept]
    else:
        parts = [seg for _, seg in collected]

    body = "\n\n\n".join(parts)
    text = f"{body}\n" if body else ""

    out_path = Path(out_py)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(text, encoding="utf-8")
    return out_path


def export_notebook_markers(ipynb: str | Path, out_py: str | Path) -> Path:
    """Export the ``# export``-marked code cells of *ipynb* into *out_py*.

    The exporter for pipeline-library notebooks (``loading.py``,
    ``scaling.py``, …) whose exportable surface is not the strict-AST
    allowlist :func:`export_notebook` enforces. A code cell is emitted
    **whole** (minus the marker line) iff one of its lines, stripped, is
    exactly ``# export``. Cells without the marker — scratch, plots,
    smoke tests — are skipped. A cell that does not parse is skipped.

    Cells are emitted in notebook order, separated by two blank lines.
    Returns the path written. The ``.ipynb``-reading wrapper over
    :func:`export_cells_markers`.
    """
    return export_cells_markers(_code_cell_sources(ipynb), out_py)


def export_cells_markers(cell_sources: Sequence[str], out_py: str | Path) -> Path:
    """Export the ``# export``-marked cells of *cell_sources* into *out_py*.

    The format-agnostic marker core (see :func:`export_notebook_markers`
    for the marker rule): *cell_sources* are plain code-cell strings,
    whether read from an ``.ipynb`` or from a percent-format ``.py``.
    Returns the path written.
    """
    parts: list[str] = []
    for src in cell_sources:
        lines = src.splitlines()
        if not any(line.strip() == _EXPORT_MARKER for line in lines):
            continue
        kept = "\n".join(line for line in lines if line.strip() != _EXPORT_MARKER).strip("\n")
        if not kept.strip():
            continue
        try:
            ast.parse(kept)
        except SyntaxError:
            continue
        parts.append(kept)

    body = "\n\n\n".join(parts)
    text = f"{body}\n" if body else ""
    out_path = Path(out_py)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(text, encoding="utf-8")
    return out_path


def notebook_imports_runtime(ipynb: str | Path) -> bool:
    """True if any code cell of *ipynb* imports from ``hpc_agent.experiment_kit``.

    A utility check — note this is *not* a reliable "is this a runnable
    experiment" signal: pipeline-library notebooks import the runtime
    seam (``current_slice`` / ``load_series``) without applying
    ``@register_run``. :func:`hpc_agent.incorporation.export_package.export_package`
    classifies notebooks by the ``@register_run`` decorator (via
    :func:`~hpc_agent.experiment_kit.discover.discover_runs`) for that reason.
    """
    for src in _code_cell_sources(ipynb):
        try:
            tree = ast.parse(src)
        except SyntaxError:
            continue
        if any(_imports_runtime(node) for node in tree.body):
            return True
    return False


def _segment(lines: list[str], node: ast.stmt) -> str:
    """Return *node*'s exact source, decorators included.

    ``ast.get_source_segment`` reports a decorated ``def``/``class`` from
    the ``def``/``class`` keyword — it omits the ``@decorator`` lines. We
    extend the start back to the first decorator so an exported
    ``@register_run`` function keeps its decorator.
    """
    start = node.lineno
    if isinstance(node, _DEF_NODES) and node.decorator_list:
        start = node.decorator_list[0].lineno
    end = node.end_lineno or start
    return "\n".join(lines[start - 1 : end])


def _is_exportable(node: ast.stmt) -> bool:
    if isinstance(node, _IMPORT_NODES + _DEF_NODES):
        return True
    if isinstance(node, ast.Assign):
        return bool(node.targets) and all(_is_upper_name(t) for t in node.targets)
    if isinstance(node, ast.AnnAssign):
        return _is_upper_name(node.target)
    return False


def _is_upper_name(target: ast.expr) -> bool:
    """True for a bare ``NAME`` target whose identifier is uppercase.

    ``str.isupper()`` ignores underscores and digits, so ``WINDOW``,
    ``TRAIN_WINDOW`` and ``_TASKS`` all qualify while ``df`` / ``model``
    do not. Tuple / subscript / attribute targets never qualify.
    """
    return isinstance(target, ast.Name) and target.id.isupper()


def _imports_runtime(node: ast.stmt) -> bool:
    """True if *node* imports from ``hpc_agent.experiment_kit`` (any submodule)."""
    if isinstance(node, ast.ImportFrom) and node.module:
        return node.module == _RUNTIME_MODULE or node.module.startswith(_RUNTIME_MODULE + ".")
    if isinstance(node, ast.Import):
        return any(
            a.name == _RUNTIME_MODULE or a.name.startswith(_RUNTIME_MODULE + ".")
            for a in node.names
        )
    return False


def _is_future_import(node: ast.stmt) -> bool:
    return isinstance(node, ast.ImportFrom) and node.module == "__future__"
