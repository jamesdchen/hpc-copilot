"""``program-init`` — materialize (or adopt) a PROGRAM pack layer from a domain seam.

The seat that builds plan item P1a (``docs/design/program-init.md``): the three-tier
pack architecture's PROGRAM edge. A domain pack ships the reusable skeleton; at
program creation this verb CONSUMES that skeleton to generate a program template,
stamps the lineage (``derived_from {pack, seam, version, sha}``) MECHANICALLY,
seals the program manifest via the generic pack re-seal, and binds the packs — one
command = a working program layer. Lineage is code-authored, never asked (P3).

Two modes:

* **create** — generate ``packs/<program>/`` fresh: a VERBATIM byte copy of the
  domain ``audit_template`` seam file (DP1 — core never interprets template
  content) plus a code-authored provenance header, a ``sweep.json`` recipe carrying
  the lineage stamp, a sealed ``manifest.json``, and binds of both packs.
* **adopt** — migrate an EXISTING program pack onto its real lineage WITHOUT
  byte-changing any content file (the signed-template migration path, memo hazard
  2): stamp the recipe, reseal the manifest, rebind ONLY the pack whose bytes
  moved. The domain pack (a lineage root) is untouched — no no-op rebind.

The check leg runs a caller-resolvable command ONLY (``spec.check`` or, absent
that, reports the domain pack's receipt slots as to-earn). Core never composes an
argv from pack content (DP2/DP4) — ``program-init`` reuses the same subprocess seat
``pack-refresh`` does.

Lives inside the ``pack`` subject, reaching only ``state.*`` + the same-subject
``ops.pack.bind_op`` / ``ops.pack.refresh_op`` — the subject-imports lint is
satisfied by construction (the ``refresh_op`` posture).
"""

