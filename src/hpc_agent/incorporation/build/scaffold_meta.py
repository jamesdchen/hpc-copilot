"""Generator-version stamp + submit-time staleness check for ``.hpc/`` scaffolds (#364).

A ``.hpc/`` scaffold generated by an *older* hpc-agent can survive an
upgrade and then fail on the cluster with a runtime ``ImportError`` —
because nothing at submit time checks that the generated scaffold's
``hpc_agent.*`` imports still resolve against the *installed* package.
The observed case: a pre-reorg ``.hpc/_build_tasks.py`` importing
``from hpc_agent.template import register_run, ...`` after
``hpc_agent.template`` was consolidated away. The only signal the user
got was a far-downstream ``ImportError`` on a compute node, not a cheap
local refusal.

hpc-agent already version-stamps sidecars / manifests / journal records;
this module adds the missing stamp + check for the **generated scaffold**:

1. :func:`stamp_scaffold_meta` writes ``.hpc/.scaffold_meta.json`` carrying
   the generating ``hpc_agent.__version__`` — called by ``build-tasks-py``
   right after it writes ``tasks.py`` / ``cli.py``.
2. :func:`check_scaffold_staleness` is the cheap, **local** (no SSH)
   submit-time check: when the installed version differs from the stamp
   (or the scaffold is unstamped) AND a generated file imports a module
   path the installed package no longer exposes, it reports the scaffold
   as stale so the caller can refuse before the expensive cluster
   round-trip. A pre-reorg ``_build_tasks.py`` is stale by construction —
   current generators emit ``tasks.py``.

The check is a **no-op when the stamp matches the installed version**: it
returns ``fresh`` without scanning a single import, so a current-version
scaffold submits with byte-identical behavior.
"""

from __future__ import annotations

import ast
import contextlib
import dataclasses
import importlib
import importlib.util
import json
import os
import tempfile
from pathlib import Path

import hpc_agent

__all__ = [
    "SCAFFOLD_META_FILENAME",
    "SCAFFOLD_META_SCHEMA_VERSION",
    "ScaffoldStaleness",
    "check_scaffold_staleness",
    "read_scaffold_meta",
    "scaffold_meta_path",
    "stamp_scaffold_meta",
]

#: Sidecar file (under ``.hpc/``) carrying the generator-version stamp. A
#: leading dot keeps it out of the user's line of sight — it is
#: framework-owned metadata, not hand-edited like ``tasks.py``.
SCAFFOLD_META_FILENAME = ".scaffold_meta.json"

#: Bumped when the *shape* of ``.scaffold_meta.json`` changes (not when the
#: generator version changes — that lives in the ``generator_version`` field).
SCAFFOLD_META_SCHEMA_VERSION = 1

#: Generated python files the current generators emit into ``.hpc/`` and that
#: this module stamps + scans for ``hpc_agent.*`` import validity.
_GENERATED_SCAFFOLD_FILES: tuple[str, ...] = ("tasks.py", "cli.py")

#: Pre-reorg generated artifacts. Current versions emit ``tasks.py`` (not
#: ``_build_tasks.py``), so the presence of any of these is stale *by
#: construction* — a dead generated file from a prior hpc-agent.
_LEGACY_SCAFFOLD_FILES: tuple[str, ...] = ("_build_tasks.py",)


def scaffold_meta_path(experiment_dir: Path) -> Path:
    """Return the path to the scaffold meta sidecar for *experiment_dir*."""
    return Path(experiment_dir) / ".hpc" / SCAFFOLD_META_FILENAME


def stamp_scaffold_meta(experiment_dir: Path, *, scaffold_files: list[str]) -> Path:
    """Stamp the generating ``hpc_agent.__version__`` into ``.hpc/.scaffold_meta.json``.

    Called by ``build-tasks-py`` after it writes the scaffold so a later
    submit (under a possibly-upgraded hpc-agent) can tell *which* version
    generated the files on disk. The write is atomic + fsync-durable —
    same recipe as the scaffold file write itself — so a crash never
    leaves a half-written stamp that the checker would misread.

    Returns the path written.
    """
    target = scaffold_meta_path(experiment_dir)
    payload = {
        "schema_version": SCAFFOLD_META_SCHEMA_VERSION,
        "generator_version": hpc_agent.__version__,
        "scaffold_files": sorted(scaffold_files),
    }
    body = json.dumps(payload, indent=2, sort_keys=True) + "\n"

    target.parent.mkdir(parents=True, exist_ok=True)
    fd, tmp_name = tempfile.mkstemp(prefix=target.name + ".", suffix=".tmp", dir=str(target.parent))
    try:
        with os.fdopen(fd, "w", encoding="utf-8") as fh:
            fh.write(body)
            fh.flush()
            with contextlib.suppress(OSError):
                os.fsync(fh.fileno())
        os.replace(tmp_name, target)
        with contextlib.suppress(OSError):
            dir_fd = os.open(str(target.parent), os.O_RDONLY)
            try:
                os.fsync(dir_fd)
            finally:
                os.close(dir_fd)
    except BaseException:
        with contextlib.suppress(OSError):
            os.unlink(tmp_name)
        raise
    return target


def read_scaffold_meta(experiment_dir: Path) -> dict | None:
    """Return the parsed scaffold meta sidecar, or ``None`` when absent/corrupt.

    A missing stamp is the legacy case (scaffold predates the stamp
    feature); a corrupt one is treated identically — "unknown generator,"
    which routes the checker into the import scan rather than trusting it.
    """
    path = scaffold_meta_path(experiment_dir)
    try:
        raw = path.read_text(encoding="utf-8")
    except (FileNotFoundError, OSError):
        return None
    try:
        data = json.loads(raw)
    except json.JSONDecodeError:
        return None
    return data if isinstance(data, dict) else None


@dataclasses.dataclass(frozen=True)
class ScaffoldStaleness:
    """Outcome of :func:`check_scaffold_staleness`.

    ``status`` is the human-facing classification; ``stale`` is the single
    boolean a caller gates on:

    * ``fresh`` — stamp matches the installed version (no import scan run),
      or the scan found every ``hpc_agent.*`` import resolvable. ``stale``
      is ``False``.
    * ``stale`` — a legacy ``_build_tasks.py`` artifact is present, or a
      generated file imports a module/symbol the installed package no
      longer exposes. ``stale`` is ``True``.

    ``reasons`` is a list of human-readable strings; ``unresolved_imports``
    carries one dict per broken import (``{file, module, missing, ...}``);
    ``legacy_artifacts`` lists any pre-reorg files found.
    """

    status: str
    stale: bool
    installed_version: str
    stamp_version: str | None
    reasons: list[str]
    unresolved_imports: list[dict[str, str]]
    legacy_artifacts: list[str]
    scanned_files: list[str]


def _hpc_imports(source: str) -> list[tuple[str, list[str]]]:
    """Extract ``hpc_agent.*`` imports from python *source* via AST.

    Returns ``[(module, [names])]`` where ``names`` is empty for a plain
    ``import hpc_agent.x`` and the imported names for ``from hpc_agent.x
    import a, b``. Only imports rooted at ``hpc_agent`` are returned — a
    stale scaffold's breakage is always against the framework's own
    surface, never the stdlib or third-party packages.
    """
    try:
        tree = ast.parse(source)
    except SyntaxError:
        # A scaffold that won't even parse is broken in a way the import
        # scan can't characterise; leave it to the import-time validators.
        return []
    out: list[tuple[str, list[str]]] = []
    for node in ast.walk(tree):
        if isinstance(node, ast.Import):
            for alias in node.names:
                if alias.name == "hpc_agent" or alias.name.startswith("hpc_agent."):
                    out.append((alias.name, []))
        elif isinstance(node, ast.ImportFrom):
            # level > 0 is a relative import; scaffolds use absolute
            # ``hpc_agent`` paths, so a relative import is never ours.
            mod = node.module or ""
            if node.level == 0 and (mod == "hpc_agent" or mod.startswith("hpc_agent.")):
                names = [a.name for a in node.names if a.name != "*"]
                out.append((mod, names))
    return out


def _check_import(module: str, names: list[str]) -> list[dict[str, str]]:
    """Verify *module* (and *names* imported from it) resolve against the install.

    Module existence is checked with :func:`importlib.util.find_spec` so a
    vanished module (the common reorg case) is caught WITHOUT importing
    anything. Only when the module resolves and named symbols were imported
    do we import it to confirm each name still exists (a submodule re-export
    counts). Each unresolved item is one dict the caller surfaces as
    evidence.
    """
    problems: list[dict[str, str]] = []
    try:
        spec = importlib.util.find_spec(module)
    except (ImportError, AttributeError, ValueError):
        spec = None
    if spec is None:
        problems.append(
            {
                "module": module,
                "missing": "module",
                "detail": f"installed hpc-agent has no module {module!r}",
            }
        )
        return problems
    if not names:
        return problems
    try:
        mod = importlib.import_module(module)
    except Exception as exc:  # noqa: BLE001 — any import failure means "won't resolve on the cluster"
        problems.append(
            {
                "module": module,
                "missing": "module",
                "detail": f"importing {module!r} failed: {type(exc).__name__}: {exc}",
            }
        )
        return problems
    for name in dict.fromkeys(names):  # de-dup, preserve order
        if hasattr(mod, name):
            continue
        # The name may be a submodule not yet bound as an attribute
        # (``from hpc_agent import errors`` before errors is imported).
        try:
            if importlib.util.find_spec(f"{module}.{name}") is not None:
                continue
        except (ImportError, AttributeError, ValueError):
            pass
        problems.append(
            {
                "module": module,
                "missing": "symbol",
                "symbol": name,
                "detail": f"{module!r} no longer exposes {name!r}",
            }
        )
    return problems


def check_scaffold_staleness(experiment_dir: Path) -> ScaffoldStaleness:
    """Local, no-SSH staleness check over the ``.hpc/`` scaffold (#364).

    Fast path: when the stamp matches the installed ``hpc_agent.__version__``
    and no legacy artifact is present, returns ``fresh`` immediately without
    scanning a single import — so a current-version scaffold is a guaranteed
    no-op.

    Otherwise (version mismatch, or an unstamped/legacy scaffold) it scans
    the generated files' ``hpc_agent.*`` imports against the installed
    package and reports ``stale`` when a legacy ``_build_tasks.py`` exists
    or any import no longer resolves. An unstamped scaffold whose imports
    all resolve is NOT flagged — "unknown generator → verify, don't refuse."
    """
    experiment_dir = Path(experiment_dir)
    installed = hpc_agent.__version__
    hpc_dir = experiment_dir / ".hpc"

    legacy = [f for f in _LEGACY_SCAFFOLD_FILES if (hpc_dir / f).is_file()]
    meta = read_scaffold_meta(experiment_dir)
    stamp_version = None
    if meta is not None:
        sv = meta.get("generator_version")
        stamp_version = sv if isinstance(sv, str) else None

    # No-op fast path: the stamp matches the installed version and there is
    # no pre-reorg artifact. Byte-identical to pre-#364 behaviour — no scan.
    if stamp_version == installed and not legacy:
        return ScaffoldStaleness(
            status="fresh",
            stale=False,
            installed_version=installed,
            stamp_version=stamp_version,
            reasons=[],
            unresolved_imports=[],
            legacy_artifacts=[],
            scanned_files=[],
        )

    reasons: list[str] = []
    unresolved: list[dict[str, str]] = []
    scanned: list[str] = []

    if legacy:
        reasons.append(
            "pre-reorg scaffold artifact "
            + ", ".join(sorted(legacy))
            + " present (current hpc-agent emits .hpc/tasks.py); the scaffold is "
            "from an older generator"
        )

    for fname in (*_GENERATED_SCAFFOLD_FILES, *_LEGACY_SCAFFOLD_FILES):
        path = hpc_dir / fname
        if not path.is_file():
            continue
        try:
            source = path.read_text(encoding="utf-8")
        except OSError:
            continue
        scanned.append(fname)
        for module, names in _hpc_imports(source):
            for problem in _check_import(module, names):
                unresolved.append({"file": f".hpc/{fname}", **problem})

    if unresolved:
        reasons.append(
            f"{len(unresolved)} import(s) in the generated scaffold no longer "
            "resolve against the installed hpc-agent"
        )

    stale = bool(legacy) or bool(unresolved)
    return ScaffoldStaleness(
        status="stale" if stale else "fresh",
        stale=stale,
        installed_version=installed,
        stamp_version=stamp_version,
        reasons=reasons,
        unresolved_imports=unresolved,
        legacy_artifacts=sorted(legacy),
        scanned_files=scanned,
    )
